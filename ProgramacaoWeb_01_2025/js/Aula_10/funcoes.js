// crie uma função para as quatro operações matemáticas básicas, adição subtração divisão e multiplicação

function soma(n1, n2){
    return `A soma dos números é: ${n1+n2}`
}

function subtração(n1, n2){
    return `${n1} - ${n2} = ${n1-n2}`
}

function divisão(n1, n2){
    return `${n1} divido por ${n2} = ${n1/n2}`
}

function multiplicação(n1, n2){
    return `${n1} vezes ${n2} = ${n1*n2}`
}

console.log(soma(5, 8))
console.log(subtração(5, 8));
console.log(divisão(5, 8));
console.log(multiplicação(5, 8));